package databean;

public class ProfileDataBean {
	private String id;
	private int profile_age;
	private String profile_context;
	private String profile_hobby;
	private String profile_img;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getProfile_age() {
		return profile_age;
	}
	public void setProfile_age(int profile_age) {
		this.profile_age = profile_age;
	}
	public String getProfile_context() {
		return profile_context;
	}
	public void setProfile_context(String profile_context) {
		this.profile_context = profile_context;
	}
	public String getProfile_hobby() {
		return profile_hobby;
	}
	public void setProfile_hobby(String profile_hobby) {
		this.profile_hobby = profile_hobby;
	}
	public String getProfile_img() {
		return profile_img;
	}
	public void setProfile_img(String profile_img) {
		this.profile_img = profile_img;
	}
	
	
}
